package org.example;

public class User {
    final String NAME;
    final String SURNAME;
    final int ID;
    final String BIRTH_DATE;

    public User(String NAME, String SURNAME, int ID, String BIRTH_DATE) {
        this.NAME = NAME;
        this.SURNAME = SURNAME;
        this.ID = ID;
        this.BIRTH_DATE = BIRTH_DATE;
    }
}
